    var qNo;
    var questionsDisplayed = [];
    var correct_ans;
    var level;
    function getNextQuestionNo(min, max) {
	while(true) {
	    qNo = Math.floor(Math.random()*(max-min)) + min;
	    for	(index = 0; index < questionsDisplayed.length; index++) {
		if (qNo == questionsDisplayed [index]) { continue; }
	    }
	    questionsDisplayed.push(qNo);
	    break;
	}
    	return qNo;
    }
    
    function nextQuestion() {
        qNo =  getNextQuestionNo(1,15);
	$.get("../getQuestion.php?qNo="+qNo, function(qHtml,status) {
		$("#question-area").html(qHtml);
	});
	
	$.get("../getAnswer.php?qNo="+qNo, function(data,status) {
		var ans_data = JSON.parse(data);
		correct_ans = ans_data["ans"];
		$('#quiz_ch1').text(ans_data['ch1']);
		$('#quiz_ch1_inp').val(ans_data['ch1']);
