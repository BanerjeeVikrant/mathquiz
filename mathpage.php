    	changeTimer();
    	myTimer = setInterval(reduceTime, 1000);	
    }
    function changeTimer(){
    	if(time == "15 Min"){
    		$('#sec').text("0");
    		$('#min').text("00");
    		
    	}else if(time == "30 Min"){
    		$('#sec').text("00");
